project = Proj(init='epsg:25832')
shapes = {}

# Create projected copy
for name, shape in _dwd.shapes.items():

    projected = []
    projected_exclude = []

    for poly in shape['polygons']:
        projected.append([tuple(project(lng, lat)) for lat, lng in poly])

    for poly in shape['exclude_polygons']:
        projected_exclude.append([tuple(project(lng, lat)) for lat, lng in poly])

    projected_shape = {
        'polygons': projected,
        'exclude_polygons': projected_exclude,
    }

    shapes[name] = projected_shape


bbox = [
    min(coord[0] for area in shapes.values() for poly in area['polygons'] for coord in poly),
    min(coord[1] for area in shapes.values() for poly in area['polygons'] for coord in poly),
    max(coord[0] for area in shapes.values() for poly in area['polygons'] for coord in poly),
    max(coord[1] for area in shapes.values() for poly in area['polygons'] for coord in poly),
]


dist_x = bbox[2] - bbox[0]
dist_y = bbox[3] - bbox[1]
img_width = int(dist_x / 100)
img_height = int(dist_y / 100)
ratio_x = img_width / dist_x
ratio_y = img_height / dist_y


def to_image_coords(x, y):
    px = int(img_width - ((bbox[2] - x) * ratio_x))
    py = int((bbox[3] - y) * ratio_y)
    return px, py


def draw_background():
    img = Image.new("RGB", (img_width, img_height), "#aaa")
    draw = ImageDraw.Draw(img)

    for name, shape in shapes.items():
        for poly in shape['polygons']:
            points = [to_image_coords(x, y) for x, y in poly]
            draw.polygon(points, outline=None, fill="rgb(0, 50, 93)")

        for poly in shape['exclude_polygons']:
            points = [to_image_coords(x, y) for x, y in poly]
            draw.polygon(points, outline=None, fill="white")

    return img


background_image = draw_background()


def draw_borders(img, width=6):
    draw = ImageDraw.Draw(img)

    line_width = width
    point_radius = int(line_width / 3)

    for name, shape in shapes.items():
        for poly in shape['polygons'] + shape['exclude_polygons']:
            points = [to_image_coords(*point) for point in poly]

            draw.line(points, fill='white', width=line_width)
            for point in points:
                draw.ellipse(
                    (point[0] - point_radius,
                     point[1] - point_radius,
                     point[0] + point_radius,
                     point[1] + point_radius),
                    fill="white")


def draw_event(event):
    img = background_image.copy()
    draw = ImageDraw.Draw(img)

    for geo in event['geometry']:
        if state_for_cell_id(geo['warn_cell_id']) not in STATES_FILTER:
            continue

        for poly in geo['polygons']:
            projected = [to_image_coords(*project(lng, lat)) for lat, lng in poly]
            draw.polygon(projected, outline=None, fill="red")

        for poly in geo['exclude_polygons']:
            projected = [to_image_coords(*project(lng, lat)) for lat, lng in poly]
            draw.polygon(projected, outline=None, fill="rgb(0, 50, 93)")

    draw_borders(img)

    return img.resize((int(img_width * .4), int(img_height * .4)), resample=Image.LANCZOS)
